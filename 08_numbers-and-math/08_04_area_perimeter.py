# Write code to display the area and perimeter
# of a rectangle that has a width of 2.4 and a height of 6.4.

length = 2.4
width = 6.4

area = length * width
area = str(area)

perimiter = (length + width) * 2
perimiter = str(perimiter)

print(f"The area of the rectagle is {area}")
print(f"The perimiter of the rectagle is {perimiter}")